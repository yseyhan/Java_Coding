package day03_MyVariables;

public class EscapeSequences {
    public static void main(String[] args) {

        // \n: starts a new line
        System.out.println("Hello Everyone");
        System.out.println("My name is Yalcin, I am from Virginia");

        System.out.println("===============================================");

        System.out.println("Hello Everyone\nMy name is Yalcin, I am from Virginia");

        System.out.println("===============================================");

        // \t:
        System.out.println("\tToday I learned Java");
        System.out.println("it was so much fun");

        System.out.println("===============================================");

        //  \\: \
        System.out.println("/"); // prints single front slash
        System.out.println("\\"); // prints single backslash

        System.out.println("===============================================");

        System.out.println("My name is 'Yalcin'");
        System.out.println("My name is \'Yalcin\'");

        System.out.println("===============================================");
        // \":
        System.out.println("My favorite show is \"Game Of Thrones\"");





    }
}
